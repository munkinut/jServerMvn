public class ListenerManagerException extends Exception {

    /**
     *
     */
    private static final long serialVersionUID = -9171597039322892439L;

    public ListenerManagerException() {
        super();
    }

    public ListenerManagerException(String arg0) {
        super(arg0);
    }

    public ListenerManagerException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public ListenerManagerException(Throwable arg0) {
        super(arg0);
    }

}
